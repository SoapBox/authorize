<?php namespace SoapBox;

class User {

	/**
	 * A unique identifier for the user
	 *
	 * @var mixed
	 */
	public $id;

	/**
	 * The username representing the user on the system
	 *
	 * @var string
	 */
	public $username;

	/**
	 * The users email address
	 *
	 * @var string
	 */
	public $email;

}
